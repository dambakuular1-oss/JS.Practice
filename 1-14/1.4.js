function треугольник(a, b, c) {
  // Проверка: существует ли треугольник с такими сторонами
  if (a <= 0 || b <= 0 || c <= 0) {
    return "Ошибка: стороны должны быть больше нуля";
  }
  if (a + b <= c || a + c <= b || b + c <= a) {
    return "Ошибка: такой треугольник не существует";
  }

  // Формула Герона
  const p = (a + b + c) / 2;             // полупериметр
  const площадь = +(Math.sqrt(p * (p - a) * (p - b) * (p - c))).toFixed(2);
  const периметр = a + b + c;

  return { площадь, периметр, полупериметр: p };
}

// Пример использования
const сторонаA = 3;
const сторонаB = 4;
const сторонаC = 5;
const результат = треугольник(сторонаA, сторонаB, сторонаC);

console.log(`Стороны: ${сторонаA}, ${сторонаB}, ${сторонаC} см`);
console.log(`Периметр:     ${результат.периметр} см`);
console.log(`Полупериметр: ${результат.полупериметр} см`);
console.log(`Площадь:      ${результат.площадь} см²`);