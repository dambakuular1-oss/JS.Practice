function прямоугольник(a, b) {
  const площадь = a * b;
  const периметр = 2 * (a + b);
  const диагональ = +(Math.sqrt(a ** 2 + b ** 2)).toFixed(2);

  return { площадь, периметр, диагональ };
}

// Пример использования
const ширина = 4;
const высота = 7;
const результат = прямоугольник(ширина, высота);

console.log(`Ширина: ${ширина} см, Высота: ${высота} см`);
console.log(`Площадь:   ${результат.площадь} см²`);
console.log(`Периметр:  ${результат.периметр} см`);
console.log(`Диагональ: ${результат.диагональ} см`);