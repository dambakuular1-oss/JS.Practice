function делители(n) {
  const результат = [];

  for (let i = 1; i <= n; i++) {
    if (n % i === 0) результат.push(i);
  }

  return `Делители числа ${n}: ${результат.join(", ")}`;
}

console.log( делители(36) );  // 1, 2, 3, 4, 6, 9, 12, 18, 36
console.log( делители(12) );  // 1, 2, 3, 4, 6, 12