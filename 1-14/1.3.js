function круг(r) {
  const площадь = +(Math.PI * r ** 2).toFixed(2);
  const длинаОкружности = +(2 * Math.PI * r).toFixed(2);
  const диаметр = 2 * r;

  return { площадь, длинаОкружности, диаметр };
}

// Пример использования
const радиус = 5;
const результат = круг(радиус);

console.log(`Радиус:           ${радиус} см`);
console.log(`Диаметр:          ${результат.диаметр} см`);
console.log(`Площадь:          ${результат.площадь} см²`);
console.log(`Длина окружности: ${результат.длинаОкружности} см`);